package com.ufps.cedcufps.dto;

public class InformePonenteDto {

	private String idCurso;
	private String nombrePonente;
	private String tipoDocumentoPonente;
	private String numDocumentoPonente;
	
	
	
	public String getIdCurso() {
		return idCurso;
	}
	public void setIdCurso(String idCurso) {
		this.idCurso = idCurso;
	}
	public String getNombrePonente() {
		return nombrePonente;
	}
	public void setNombrePonente(String nombrePonente) {
		this.nombrePonente = nombrePonente;
	}
	public String getTipoDocumentoPonente() {
		return tipoDocumentoPonente;
	}
	public void setTipoDocumentoPonente(String tipoDocumentoPonente) {
		this.tipoDocumentoPonente = tipoDocumentoPonente;
	}
	public String getNumDocumentoPonente() {
		return numDocumentoPonente;
	}
	public void setNumDocumentoPonente(String numDocumentoPonente) {
		this.numDocumentoPonente = numDocumentoPonente;
	}
	
	
}
