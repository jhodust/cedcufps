package com.ufps.cedcufps.dto;

public class InformeSniesDto {

	private Long id;
	private String fechaInicio;
	private String fechaFin;
	private String informeCursos;
	private String informeEduContinua;
	private String informeParticipantes;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getFechaInicio() {
		return fechaInicio;
	}
	public void setFechaInicio(String fechaInicio) {
		this.fechaInicio = fechaInicio;
	}
	public String getFechaFin() {
		return fechaFin;
	}
	public void setFechaFin(String fechaFin) {
		this.fechaFin = fechaFin;
	}
	public String getInformeCursos() {
		return informeCursos;
	}
	public void setInformeCursos(String informeCursos) {
		this.informeCursos = informeCursos;
	}
	public String getInformeEduContinua() {
		return informeEduContinua;
	}
	public void setInformeEduContinua(String informeEduContinua) {
		this.informeEduContinua = informeEduContinua;
	}
	public String getInformeParticipantes() {
		return informeParticipantes;
	}
	public void setInformeParticipantes(String informeParticipantes) {
		this.informeParticipantes = informeParticipantes;
	}
	
	
}
