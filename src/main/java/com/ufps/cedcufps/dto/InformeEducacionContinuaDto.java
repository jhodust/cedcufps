package com.ufps.cedcufps.dto;

public class InformeEducacionContinuaDto {
	
	private String anio;
	private String semestre;
	private String codigoCurso;
	private String numHoras;
	private String idTipoCurso;
	private String valorCurso;
	private String idTipoDocumentoResponsable;
	private String numDocumentoResponsable;
	private String idTipoBeneficiario;
	private String cantidadBeneficiarios;
	
	public String getAnio() {
		return anio;
	}
	public void setAnio(String anio) {
		this.anio = anio;
	}
	public String getSemestre() {
		return semestre;
	}
	public void setSemestre(String semestre) {
		this.semestre = semestre;
	}
	public String getCodigoCurso() {
		return codigoCurso;
	}
	public void setCodigoCurso(String codigoCurso) {
		this.codigoCurso = codigoCurso;
	}
	public String getNumHoras() {
		return numHoras;
	}
	public void setNumHoras(String numHoras) {
		this.numHoras = numHoras;
	}
	public String getIdTipoCurso() {
		return idTipoCurso;
	}
	public void setIdTipoCurso(String idTipoCurso) {
		this.idTipoCurso = idTipoCurso;
	}
	public String getValorCurso() {
		return valorCurso;
	}
	public void setValorCurso(String valorCurso) {
		this.valorCurso = valorCurso;
	}
	public String getIdTipoDocumentoResponsable() {
		return idTipoDocumentoResponsable;
	}
	public void setIdTipoDocumentoResponsable(String idTipoDocumentoResponsable) {
		this.idTipoDocumentoResponsable = idTipoDocumentoResponsable;
	}
	public String getNumDocumentoResponsable() {
		return numDocumentoResponsable;
	}
	public void setNumDocumentoResponsable(String numDocumentoResponsable) {
		this.numDocumentoResponsable = numDocumentoResponsable;
	}
	public String getIdTipoBeneficiario() {
		return idTipoBeneficiario;
	}
	public void setIdTipoBeneficiario(String idTipoBeneficiario) {
		this.idTipoBeneficiario = idTipoBeneficiario;
	}
	public String getCantidadBeneficiarios() {
		return cantidadBeneficiarios;
	}
	public void setCantidadBeneficiarios(String cantidadBeneficiarios) {
		this.cantidadBeneficiarios = cantidadBeneficiarios;
	}
	
	
}
