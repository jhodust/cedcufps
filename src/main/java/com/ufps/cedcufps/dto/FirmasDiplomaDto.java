package com.ufps.cedcufps.dto;

public class FirmasDiplomaDto {

	private Long id;
	private int xCargo;
	private int yCargo;
	private int xNombre;
	private int yNombre;
	private String nombre;
	private String cargo;
	private String imagen;
	private int x;
	private int y;
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public int getxCargo() {
		return xCargo;
	}
	public void setxCargo(int xCargo) {
		this.xCargo = xCargo;
	}
	public int getyCargo() {
		return yCargo;
	}
	public void setyCargo(int yCargo) {
		this.yCargo = yCargo;
	}
	public int getxNombre() {
		return xNombre;
	}
	public void setxNombre(int xNombre) {
		this.xNombre = xNombre;
	}
	public int getyNombre() {
		return yNombre;
	}
	public void setyNombre(int yNombre) {
		this.yNombre = yNombre;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getCargo() {
		return cargo;
	}
	public void setCargo(String cargo) {
		this.cargo = cargo;
	}
	public String getImagen() {
		return imagen;
	}
	public void setImagen(String imagen) {
		this.imagen = imagen;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
}
